class allHeroes {
    constructor() {
        this.x = null;
        this.y = null;
        this.moveTo = 'right';
        this.numbStep = 4;
    }

    chengeStep() {
        if (this.numbStep === 1) {
            this.numbStep = 4;
        } else {
            this.numbStep--;
        }
    }
}

export class Pacman extends allHeroes {
    constructor() {
        super();
        this.side = 'right';
        this.startGame = true;
        this.immortality = 0;
    }

    step(y1, x1, map) {
        map[this.y][this.x] = 1;
        this.x += x1;
        this.y += y1;
        map[this.y][this.x] = 3;
        if (!this.startGame) {
            this.chengeStep();
        }
    }
}

export class Ghost extends allHeroes {
    constructor() {
        super();
        this.stepFood = false;
    }

    step(y1, x1, id, map) {

        if (this.stepFood) {
            map[this.y][this.x] = 2;
            this.stepFood = false;
        } else {
            map[this.y][this.x] = 1;
        }
        this.x += x1;
        this.y += y1;
        const nextCellFood = map[this.y][this.x] === 2;
        map[this.y][this.x] = 4 + id / 10;
        this.stepFood = nextCellFood;
        if (y1 || x1) {
            this.chengeStep();
        }
    }
}

